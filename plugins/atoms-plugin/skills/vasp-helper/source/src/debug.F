#include "symbol.inc"
#ifdef VASP_DEBUG
MODULE debug

   USE prec
   USE debug_struct
   IMPLICIT NONE

   CONTAINS

   SUBROUTINE DEBUG_READER(DBG, LOPEN, IU5, IU0)
      USE reader_tags, ONLY: PROCESS_INCAR, WRITEXMLINCAR
      IMPLICIT NONE
      TYPE(debug_settings) :: DBG
      INTEGER :: IU5
      INTEGER :: IU0
      LOGICAL :: LOPEN
      ! Local variables
      INTEGER :: IERR

      DBG%REQUEST_EXIT_AT_STEP = -1
      CALL PROCESS_INCAR(LOPEN, IU0, IU5, 'REQUEST_EXIT_AT_STEP', DBG%REQUEST_EXIT_AT_STEP, IERR, WRITEXMLINCAR)

      DBG%REQUEST_EXIT_ON_RANK = -1
      CALL PROCESS_INCAR(LOPEN, IU0, IU5, 'REQUEST_EXIT_ON_RANK', DBG%REQUEST_EXIT_ON_RANK, IERR, WRITEXMLINCAR)

      DBG%WRITE_STOPCAR_AT_STEP = -1
      CALL PROCESS_INCAR(LOPEN, IU0, IU5, 'WRITE_STOPCAR_AT_STEP', DBG%WRITE_STOPCAR_AT_STEP, IERR, WRITEXMLINCAR)
   END SUBROUTINE DEBUG_READER

END MODULE debug
#endif
