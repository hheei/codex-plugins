         MAG_NORM=SQRT(ABS(DHTOT(I,2)*DHTOT(I,2)+DHTOT(I,3)*DHTOT(I,3)+DHTOT(I,4)*DHTOT(I,4)))
         ! introduce regularization for m nabla m / |m|
