         NABMAG(1) = (ANAB1U* DWORK(I,1) - ANAB1D* DWORK(I,2))*0.5_q 
         NABMAG(2) = (ANAB2U* DWORK(I,1) - ANAB2D* DWORK(I,2))*0.5_q 
