                  CALL VDW_NONLOC(DWORK4,DWORK5,DWORK6,DWORK7,EXC,GRIDC,LATT_CUR,STRESS,2,XC%IVDW_NL,XC%ZAB_VDW,XC%GAMMA_VDW,XC%ALPHA_VDW)
